// نظام المفضلة
// ========================================
function loadFavorites() {
    return JSON.parse(localStorage.getItem('favorites') || '[]');
}

function saveFavorites(favorites) {
    localStorage.setItem('favorites', JSON.stringify(favorites));
}

function updateFavoritesCount() {
    const favorites = loadFavorites();
    const countElement = document.getElementById('favorites-count');
    if (countElement) {
        countElement.textContent = favorites.length;
    }
}

function toggleFavorite() {
    if (!currentDua) return;
    
    const favorites = loadFavorites();
    const duaIndex = favorites.findIndex(fav => fav.id === currentDua.id);
    
    if (duaIndex !== -1) {
        // إزالة من المفضلة
        favorites.splice(duaIndex, 1);
        showNotification('تم إزالة الدعاء من المفضلة');
    } else {
        // إضافة للمفضلة
        favorites.push(currentDua);
        showNotification('تم إضافة الدعاء للمفضلة');
    }
    
    saveFavorites(favorites);
    updateFavoritesCount();
    updateFavoriteIcon();
}

function updateFavoriteIcon() {
    if (!currentDua) return;
    
    const favorites = loadFavorites();
    const isFavorite = favorites.some(fav => fav.id === currentDua.id);
    const favoriteIcon = document.getElementById('favorite-icon');
    
    if (favoriteIcon) {
        favoriteIcon.style.fill = isFavorite ? '#FF6B6B' : 'none';
        favoriteIcon.style.stroke = isFavorite ? '#FF6B6B' : 'currentColor';
    }
}

function showFavorites() {
    const favorites = loadFavorites();
    const grid = document.getElementById('favorites-grid');
    
    if (favorites.length === 0) {
        grid.innerHTML = `
            <div class="empty-state">
                <span class="icon">💔</span>
                <h3>لا توجد أدعية مفضلة</h3>
                <p>أضف بعض الأدعية إلى المفضلة لتسهيل الوصول إليها</p>
            </div>
        `;
    } else {
        grid.innerHTML = favorites.map(dua => `
            <div class="dua-card" onclick="openDuaFromFavorites(${dua.id}, '${dua.title}', '${dua.text.replace(/'/g, "\\'")}')">
                <h3>${dua.title}</h3>
                <p class="dua-preview">${dua.text.substring(0, 100)}...</p>
                <button class="remove-favorite" onclick="event.stopPropagation(); removeFromFavorites(${dua.id})">إزالة</button>
            </div>
        `).join('');
    }
    
    showPage('favorites-page');
}

function removeFromFavorites(duaId) {
    const favorites = loadFavorites();
    const updatedFavorites = favorites.filter(fav => fav.id !== duaId);
    saveFavorites(updatedFavorites);
    updateFavoritesCount();
    showFavorites();
    showNotification('تم إزالة الدعاء من المفضلة');
}

function openDuaFromFavorites(id, title, text) {
    currentDua = { id: parseInt(id), title, text };
    document.getElementById('dua-title').textContent = title;
    document.getElementById('dua-text').textContent = text;
    updateFontSize();
    updateFavoriteIcon();
    showPage('dua-reader-page');
}

function showNotification(message) {
    const notification = document.createElement('div');
    notification.className = 'notification';
    notification.textContent = message;
    document.body.appendChild(notification);
    
    setTimeout(() => {
        notification.classList.add('show');
    }, 100);
    
    setTimeout(() => {
        notification.classList.remove('show');
        setTimeout(() => {
            document.body.removeChild(notification);
        }, 300);
    }, 2000);
}

