function loadSettings() {
    const savedFontSize = localStorage.getItem('fontSize');
    const savedTheme = localStorage.getItem('theme');
    
    if (savedFontSize) {
        fontSize = parseInt(savedFontSize);
    }
    
    if (savedTheme === 'dark') {
        isDarkMode = true;
        document.body.classList.add('dark-mode');
        updateThemeIcon();
    }
}

// ========================================
// عرض الأقسام الرئيسية
// ========================================
function renderSections() {
    console.log('بدء عرض الأقسام');
    
    const grid = document.getElementById('sections-grid');
    if (!grid) {
        console.error('لم يتم العثور على عنصر sections-grid');
        return;
    }
    
    grid.innerHTML = '';
    
    console.log('عدد الأقسام:', sections.length);
    
    sections.forEach((section, index) => {
        console.log('عرض القسم:', section.title);
        
        const card = document.createElement('div');
        card.className = 'section-card';
        card.onclick = () => openSection(section.id);
        
        card.innerHTML = `
            <div class="section-icon">${section.icon}</div>
            <h3>${section.title}</h3>
            <p>${section.count} دعاء</p>
        `;
        
        grid.appendChild(card);
    });
    
    console.log('تم عرض جميع الأقسام بنجاح');
}

// ========================================
// فتح قسم معين
// ========================================
function openSection(sectionId) {
    currentSection = sectionId;
    const section = sections.find(s => s.id === sectionId);
    
    document.getElementById('section-title').textContent = section.title;
    
    const list = document.getElementById('duas-list');
    list.innerHTML = '';
    
    const sectionDuas = duas[sectionId] || [];
    
    sectionDuas.forEach(dua => {
        const item = document.createElement('div');
        item.className = 'dua-item';
        item.onclick = () => openDua(dua);
        
        item.innerHTML = `
            <h3>${dua.title}</h3>
            <svg xmlns="http://www.w3.org/2000/svg" width="20" height="20" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round">
                <polyline points="9 18 15 12 9 6"></polyline>
            </svg>
        `;
        
        list.appendChild(item);
    });
    
    showPage('duas-page');
}

// ========================================
// فتح دعاء للقراءة
// ========================================
function openDua(dua) {
    document.getElementById('dua-title').textContent = dua.title;
    document.getElementById('dua-text').textContent = dua.text;
    document.getElementById('dua-text').style.fontSize = fontSize + 'px';
    
    showPage('dua-reader-page');
}

// ========================================
// إدارة التنقل بين الصفحات
